const add=(a,b)=>{
    return (a+b).toString();
}
const substract=(a,b)=>{
    return (a-b).toString();
}
const multiply=(a,b)=>{
    return (a*b).toString();
}
const division=(a,b)=>{
    return (a/b).toString();
}
module.exports.add=add;
module.exports.substract=substract;
module.exports.multiply=multiply;
module.exports.division=division;